#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		a;
	char	*c;

	a = ft_strlen(s1);
	if (!(c = malloc(a + 1)))
		return (NULL);
	while (a >= 0)
	{
		c[a] = s1[a];
		a--;
	}
	return (c);
}
